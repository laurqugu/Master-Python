"""
Ejercicio 4:
Pedir dos números al usuario y hacer todas las 
operaciones básicas de una calculadora y mostrarlo por pantalla
"""

num1 = int(input("Ingrese el primer número: "))
num2 = int(input("Ingrese el segundo número: "))

print("#### CALCULADORA ####")
print("Suma: " + str(num1+num2))
print("Resta: " + str(num1-num2))
print("Multiplicación: " + str(num1*num2))
print("División: " + str(num1/num2))